"""
Condições IF, ELIF e ELSE
"""

if False:
    print('Verdadeiro')

    num_1 = 2
    num_2 = 4

    print(num_1 + num_2)

elif True:
    print("Agora é verdadeiro")  # Se tiver True

elif False:
    print("Também é verdadeiro")  # Se tiver True

else:
    print("Não é verdadeiro")  # Todas sendo false, exec else
